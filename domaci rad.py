#1.1 zadatak-osjećaji
osjecaj = input("Kako se osjecas?")


if osjecaj == "sretan":
    print("Super je što si sretan!")
elif osjecaj == "nervozan":
    print("Duboko udahni 3 puta.")
elif osjecaj == "uzbuđen":
    print("Smiri se,sve će biti uredu.")
elif osjecaj == "miran":
    print("Super!")
elif osjecaj == "tužan":
    print("Kako ti mogu pomoći?")
else:
    print("Ne prepoznajem ovaj osjecaj.")
