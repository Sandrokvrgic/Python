#KALKULATOR
prvi = input("Upiši prvi broj:")
drugi = input("Upiši drugi broj:")
znak = input("Upiši +,-,*,/:")
if znak == "+":
    print("prvi + drugi")
elif znak == "-":
    print("prvi - drugi")
elif znak == "*":
    print("prvi * drugi")
elif znak == "-":
    print("prvi / drugi")
else:
    print("ERROR")
